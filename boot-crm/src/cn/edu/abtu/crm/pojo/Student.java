package cn.edu.abtu.crm.pojo;

public class Student {
	private Long id;
	private String stu_name;
	private String stu_politic;
	private String stu_major;
	private String stu_num;
	private String stu_sex;
	private String stu_time;

	public Long getId() {
		return id;
	}

	public void setId(Long id) {
		this.id = id;
	}

	public String getStu_name() {
		return stu_name;
	}

	public void setStu_name(String stu_name) {
		this.stu_name = stu_name;
	}

	public String getStu_politic() {
		return stu_politic;
	}

	public void setStu_politic(String stu_politic) {
		this.stu_politic = stu_politic;
	}

	public String getStu_major() {
		return stu_major;
	}

	public void setStu_major(String stu_major) {
		this.stu_major = stu_major;
	}

	public String getStu_num() {
		return stu_num;
	}

	public void setStu_num(String stu_num) {
		this.stu_num = stu_num;
	}

	public String getStu_sex() {
		return stu_sex;
	}

	public void setStu_sex(String stu_sex) {
		this.stu_sex = stu_sex;
	}

	public String getStu_time() {
		return stu_time;
	}

	public void setStu_time(String stu_time) {
		this.stu_time = stu_time;
	}

}
